#include "shared_libft.h"

int			isstart(char *s1, char *s2)
{
	int		i;

	i = -1;
	if (!*s2 || !*s1)
		return (0);
	while (s2[++i])
		if (s1[i] != s2[i])
			return (0);
	return (1);
}
