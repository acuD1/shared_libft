#include "shared_libft.h"

int		skip_opt(char **argv)
{
	int		i;

	i = 1;
	while (argv[i] && argv[i][0] == '-')
	{
		if (ft_strcmp(argv[i], "--"))
			return (i);
		i++;
	}
	return (i);
}
