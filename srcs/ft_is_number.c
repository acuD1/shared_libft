#include "shared_libft.h"

int8_t	ft_is_number(const char *s)
{
	size_t	i;

	i = 0;
	while (s && s[i])
	{
		if (s[i] < '0' || s[i] > '9')
			return (-1);
		i++;
	}
	return (0);
}
