#include "shared_libft.h"

void	*ft_strlower(char *s)
{
	size_t		i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] >= 'A' && s[i] <= 'Z')
			s[i] = (char)ft_tolower(s[i]);
		i++;
	}
	return (s);
}
